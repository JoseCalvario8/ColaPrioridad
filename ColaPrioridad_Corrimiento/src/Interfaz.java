    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        TDA_Prioridad aux = new TDA_Prioridad();

        aux.setDato(jTextField1.getText().charAt(0));
        aux.setNivel((int) jSpinner1.getValue());
        boolean respuesta = obj.insertar(aux);
        if (respuesta == true) {
            JOptionPane.showMessageDialog(this, "Exito al INSERTAR");
        } else {
            JOptionPane.showMessageDialog(this, "ERROR no se puede insertar");
        }
        jTextField1.setText("");
        jSpinner1.setValue(1);
        mostrarCola();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        boolean respuesta = obj.eliminar();
        if (respuesta == true) {
            JOptionPane.showMessageDialog(this, "Exito al ELIMINAR");
        } else {
            JOptionPane.showMessageDialog(this, "ERROR al ELIMINAR");
        }
        mostrarCola();
    }//GEN-LAST:event_jButton2ActionPerformed
    private void mostrarCola() {

        String cad = "";
        for (int i = obj.getINI(); i <= obj.getFIN(); i++) {
            cad += "[" + i + "] = " + obj.getValor(i) + "  ";
        }
        jTextArea1.setText(cad);
    }

